/*global localStorage: false*/
/*global document: false*/
/*global window: false*/
function changeProfile1() {
	var firstNameInput = document.getElementById("firstNameEntry1").innerHTML;
    localStorage.setItem("firstName", firstNameInput);
        
    var lastNameInput = document.getElementById("lastNameEntry1").innerHTML;
    localStorage.setItem("lastName", lastNameInput);
    
    var barberPic = document.getElementById("barb1").src;
    localStorage.setItem("profilePic", barberPic);
    
    window.location.href = "barberProfile.html"
}

